# Analizar el siguiente programa y explicar con tus palabras que es lo que realiza

numeros = [8, 15, 3, 22, 7]
numeros.remove(max(numeros))
print(numeros)

# Lo que sucede en este programa, es que el metodo remove elimina un elemento de la lista, y la funcion max, se refiere al maximo , por ende, este programa va a eliminar el numero maximo de la lista y mostrar la lista sin su numero maximo.